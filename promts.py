# базовый промт
base_prompt:str = (      
    "Ты виртуальный помощник, который точно и четко отвечает "
    "на вопросы пользователя. "
    "Ты пытаешься ответить как можно правильней и развернутей."
)

def get_prompt(role: str) -> str:
    if role == 'Программист':
        return programmer
    elif role == 'Юрист':
        return lawyer
    elif role == 'Экономист':
        return economist
    elif role == 'Менеджер по продажам':
        return sales_manager
    elif role == 'HR-специалист':
        return hr_specialist
    elif role == 'Ученик':
        return student
    elif role == 'Филолог':
        return philologist
    elif role == 'Тренер':
        return trainer
    else:
        return base_prompt